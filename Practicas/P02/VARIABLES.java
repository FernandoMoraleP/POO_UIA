public class VARIABLES {

    public static void main(String[] args) {

        byte a = 1;
        short b = 2;
        int c = 1244;
        long d = 12345;
        float r = 10.0f;
        double f = 1.2;
        char g = 'w';
        String hello = "hola";

        System.out.println("El valor para la variable de tipo byte es:  " + a);
        System.out.println("El valor para la variable de tipo short es:  " + b);
        System.out.println("El valor para la variable de tipo int es:  " + c);
        System.out.println("El valor para la variable de tipo long es:  " + d);
        System.out.println("El valor para la variable de tipo float es:  " + r);
        System.out.println("El valor para la variable de tipo double es:  " + f);
        System.out.println("El valor para la variable de tipo char es:  " + g);
        System.out.println("El valor para la variable de tipo String es:  " + hello);
    }

}